using System;

class Program
{
    static void Main()
    {
        // Inicializamos a variável
        int numero = 1;

        // Laço while que executa enquanto 'numero' for menor ou igual a 10
        while (numero <= 10)
        {
            Console.WriteLine(numero);
            numero++; // Incrementamos a variável
        }
    }
}
